#!/usr/bin/perl

print("\nASCII, chr and reverse Demo");

$ascii_value = ord("e");  	# now 101
$character = chr(101); 	# now "e"

$revstring = reverse ("India");

print("\nascii_value-->$ascii_value");
print("\ncharacter-->$character");
print("\nreverse string-->$revstring");

print("\n");
